// 우항과 좌항의 값을 비교 후 true, flase 반환 (조건문에 사용)

let A = 10;
let B = 20;

console.log(`A>B : ${A > B}`); //A가 B보다 크다 = false
console.log(`A>=B : ${A >= B}`); //A가 B보다 크거나 같다 = false
console.log(`A<B : ${A < B}`); //A가 B보다 작다 = true
console.log(`A<=B : ${A <= B}`); //A가 B보다 작거나 같다 = true
// A = "20";
console.log(`A==B : ${A == B}`); //A와 B가 같다 = false
console.log(`A!=B : ${A != B}`); //A와 B가 같지 않다 = true
console.log(`A===B : ${A === B}`); //A와 B가 자료형까지 같다 = false
console.log(`A!==B : ${A !== B}`); //A와 B가 자료형까지 같지 않다 = true
